export async function seedTemplates() {
  console.log('🌱 Seeding 269 premium templates...');

  // Preserve the 3 original legacy templates; replace everything else
  await prisma.resumeTemplate.deleteMany({
    where: { id: { notIn: ['professional', 'modern', 'executive'] } },
  });

  let created = 0;
  for (const seed of templateSeeds) {
    await prisma.resumeTemplate.upsert({
      where: { id: seed.id },
      update: seed,
      create: seed,
    });
    created++;
  }

  console.log(`✅ ${created} premium templates seeded successfully.`);
  console.log('   Layouts used: 28 distinct layout types (15 original + 5 new + 8 additional) across 6 categories.');
}
